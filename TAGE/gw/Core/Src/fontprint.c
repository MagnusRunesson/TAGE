#define ITOABUF_COUNT (10) // We can do this many itoa "simultaneously"
#define ITOABUF_LEN (12) // 32 bits is 10 digits, plus a - sign, plus ending 0

char itoabuff[ITOABUF_LEN*ITOABUF_COUNT];
int itoabuffactive = 0;

char* vtoaui(unsigned int _i, int _base, int _padding)
{
    if(_padding > ITOABUF_LEN-1) {
        _padding = ITOABUF_LEN-1;
    }
    
    char* pszRet = &itoabuff[itoabuffactive + ITOABUF_LEN-1];
    *pszRet = 0;

    unsigned int value = _i;
    
    do
    {
        char v = value % _base;
        pszRet--;
        if(v >= 10) {
            *pszRet = 'a' + (v-10);
        } else {
            *pszRet = '0' + v;
        }
        value /= _base;
        _padding--;
    } while(value > 0);
    
    while(_padding > 0) {
        pszRet--;
        *pszRet = '0';
        _padding--;
    }
    
    itoabuffactive += ITOABUF_LEN;
    if(itoabuffactive >= ITOABUF_LEN*ITOABUF_COUNT) {
        itoabuffactive = 0;
    }
    
    return pszRet;
}

char* vtoasi(int _i, int _base, int _padding)
{
    if(_padding > ITOABUF_LEN-2) {
        _padding = ITOABUF_LEN-2;
    }
    
    char* pszRet = &itoabuff[itoabuffactive + ITOABUF_LEN-1];
    *pszRet = 0;
    
    int value = _i;
    
    char negsign = 0;
    if(value < 0) {
        negsign = '-';
        value = -value;
    }
    
    do
    {
        char v = value % _base;
        pszRet--;
        if(v >= 10) {
            *pszRet = 'a' + (v-10);
        } else {
            *pszRet = '0' + v;
        }
        value /= _base;
        _padding--;
    } while(value > 0);
    
    while(_padding > 0) {
        pszRet--;
        *pszRet = '0';
        _padding--;
    }
    
    if(negsign) {
        pszRet--;
        *pszRet = negsign;
    }
    
    itoabuffactive += ITOABUF_LEN;
    if(itoabuffactive >= ITOABUF_LEN*ITOABUF_COUNT) {
        itoabuffactive = 0;
    }
    
    return pszRet;
}


